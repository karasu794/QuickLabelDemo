-- デバッグ用: トリガーを一時的に無効化
-- 問題の切り分けのため、新規ユーザー登録時のprofilesテーブル挿入トリガーを無効にします

-- トリガーを削除（一時的）
DROP TRIGGER IF EXISTS on_auth_user_created ON auth.users;

-- 関数も削除（一時的）
DROP FUNCTION IF EXISTS public.handle_new_user();

-- =====================================================
-- 使用方法：
-- 1. この内容をSupabaseの「SQL Editor」で実行
-- 2. 新規ユーザー登録をテスト
-- 3. エラーが解消されるか確認
-- 4. 確認後、元のトリガーを再度有効化
-- =====================================================

